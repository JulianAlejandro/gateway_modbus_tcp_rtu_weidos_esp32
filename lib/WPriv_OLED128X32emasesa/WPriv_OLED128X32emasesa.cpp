// Función que recibe U8G2 por referencia
void showWeidmullerApplication(U8G2 &u8g2Ref, const unsigned char* logo, const char* mainTitle, const char* subText) 
  {
  u8g2Ref.clearBuffer();
  u8g2Ref.sendBuffer();
  //vTaskDelay(pdMS_TO_TICKS(VISU_SCREEN_TRANSITION_TIME));
  // Fuente grande para el título
  const int logo_w = ICON_WIDTH;
  const int logo_h = ICON_HEIGHT;
  const int spacing = ICON_SPACING;

  u8g2Ref.setFont(u8g2_font_logisoso16_tf);
  int text_w = u8g2Ref.getStrWidth(mainTitle);
  int text_h = u8g2Ref.getMaxCharHeight();

  int total_w = logo_w + spacing + text_w;
  int x_center = (128 - total_w) / 2;
  int y_logo = 4; // logo en la parte superior
  int y_text = y_logo +16 ;

  // --- 1️⃣ ANIMAR LOGO DESDE LA IZQUIERDA ---
  for (int x = -logo_w; x <= x_center; x += 2) {
    u8g2Ref.clearBuffer();
    u8g2Ref.drawXBMP(x, y_logo, logo_w, logo_h, logo);
    u8g2Ref.sendBuffer();
    //vTaskDelay(pdMS_TO_TICKS(25));
  }

  // --- 2️⃣ ANIMAR TÍTULO (fade-in estilo “escribiendo”) ---
  for (int fade = 0; fade <= 10; fade++) {
    u8g2Ref.clearBuffer();
    u8g2Ref.drawXBMP(x_center, y_logo, logo_w, logo_h, logo);

    int chars = map(fade, 0, 10, 0, strlen(mainTitle));
    String partial = String(mainTitle).substring(0, chars);
    u8g2Ref.drawStr(x_center + logo_w + spacing, y_text, partial.c_str());

    u8g2Ref.sendBuffer();
    //vTaskDelay(pdMS_TO_TICKS(100));
    }

  // --- 3️⃣ DIBUJAR TODO FINAL ---
  u8g2Ref.clearBuffer();
  u8g2Ref.drawXBMP(x_center, y_logo, logo_w, logo_h, logo);
  u8g2Ref.drawStr(x_center + logo_w + spacing, y_text, mainTitle);

  // Subtítulo debajo
  if (subText && strlen(subText) > 0) {
    u8g2Ref.setFont(u8g2_font_6x10_tf); // fuente pequeña
    int sub_w = u8g2Ref.getStrWidth(subText);
    int sub_x = (128 - sub_w) / 2; // centrar
    int sub_y = 30;    // debajo del logo
    u8g2Ref.drawStr(x_center + logo_w + spacing, sub_y, subText);
  }
  u8g2Ref.sendBuffer();

  //vTaskDelay(pdMS_TO_TICKS(VISU_SCREEN_SHOW_TIME)); 
  }

void hideWeidmullerApplication(U8G2 &u8g2Ref, const unsigned char* logo,const char* mainTitle,const char* subText)
{
  const int logo_w = ICON_WIDTH;
  const int logo_h = ICON_HEIGHT;
  const int spacing = ICON_SPACING;

  // --- Mismos cálculos de posición que en showWeidmullerApplication ---
  u8g2Ref.setFont(u8g2_font_logisoso16_tf);
  int text_w = u8g2Ref.getStrWidth(mainTitle);
  int text_h = u8g2Ref.getMaxCharHeight();

  int total_w = logo_w + spacing + text_w;
  int x_center = (128 - total_w) / 2;
  int y_logo = 4; // logo en la parte superior
  int y_text = y_logo +16 ;

  // --- 1️⃣ ANIMAR DESAPARICIÓN DEL TEXTO (como si se “borrara”) ---
  for (int fade = 10; fade >= 0; fade--) {
    u8g2Ref.clearBuffer();
    u8g2Ref.drawXBMP(x_center, y_logo, logo_w, logo_h, logo);

    int chars = map(fade, 0, 10, 0, strlen(mainTitle));
    String partial = String(mainTitle).substring(0, chars);
    u8g2Ref.setFont(u8g2_font_logisoso16_tf);
    u8g2Ref.drawStr(x_center + logo_w + spacing, y_text, partial.c_str());

    // Subtítulo (también se desvanece antes del logo)
    if (subText && strlen(subText) > 0 && fade > 2) {
      u8g2Ref.setFont(u8g2_font_6x10_tf);
      int sub_w = u8g2Ref.getStrWidth(subText);
      int sub_x = (128 - sub_w) / 2;
      int sub_y = 30;
      u8g2Ref.drawStr(x_center + logo_w + spacing, sub_y, subText);
    }

    u8g2Ref.sendBuffer();
    //vTaskDelay(pdMS_TO_TICKS(80));

  }

  // --- 2️⃣ ANIMAR LOGO SALIENDO POR LA DERECHA ---
  for (int x = x_center; x <= 128; x += 2) {
    u8g2Ref.clearBuffer();
    u8g2Ref.drawXBMP(x, y_logo, logo_w, logo_h, logo);
    u8g2Ref.sendBuffer();
    //vTaskDelay(pdMS_TO_TICKS(15));
  }

  // --- 3️⃣ LIMPIAR PANTALLA ---
  u8g2Ref.clearBuffer();
  u8g2Ref.sendBuffer();
}


